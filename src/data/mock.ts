// 辅助方法：根据 ID 获取培训师
export function getTrainerById(id: number): Trainer | undefined {
  return trainers.find(t => t.id === id)
}

// 辅助方法：根据 ID 获取课程
export function getCourseById(id: number): Course | undefined {
  return courses.find(c => c.id === id)
}

// 辅助方法：获取培训师的所有课程
export function getCoursesByTrainerId(trainerId: number): Course[] {
  return courses.filter(c => c.trainer_id === trainerId)
}

// 辅助方法：根据分类获取课程
export function getCoursesByCategoryId(categoryId: number): Course[] {
  return courses.filter(c => c.category_id === categoryId)
}

// 辅助方法：获取精选培训师
export function getFeaturedTrainers(): Trainer[] {
  return trainers.filter(t => t.featured)
}

// 辅助方法：获取精选课程
export function getFeaturedCourses(): Course[] {
  return courses.filter(c => c.featured)
}

// 辅助方法：获取所有城市（去重）
export function getAllCities(): string[] {
  return [...new Set(trainers.map(t => t.city))]
}

// 辅助方法：获取所有专长（去重）
export function getAllSpecialties(): string[] {
  return [...new Set(trainers.flatMap(t => t.specialties))]
}
